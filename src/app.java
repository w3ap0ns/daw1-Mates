public class app {

    public static void main(String[] arg){

        /*Escribe un programa en Java (Mates) que calcule y muestre el valor de la
        suma, resta, producto, división y resto, de dos números enteros.*/

        // Declaración de variables
        int x=6,y=3;

        // ********** Operaciónes *********

        //Suma
        int suma = x + y;
        System.out.printf("La suma de %s y %s es igual a: %s %n",x,y,suma);

        //Resta
        int resta = x - y;
        System.out.printf("La resta de %s y %s es igual a: %s %n",x,y,resta);

        //Multiplicación
        int multiplicacion = x * y;
        System.out.printf("La multiplicación de %s y %s es igual a: %s %n",x,y,multiplicacion);

        //División
        int division = x / y;
        System.out.printf("La división de %s y %s es igual a: %s %n",x,y,division);

    } // public static void main(String[] arg)

} // public class app
